#include<iostream>
using namespace std;

int main() {
    // Find maximum between Two numbers:-
    int a,b;
    cout << "Enter the value of a: ";
    cin >> a;
    cout << "Enter the value of b: ";
    cin >> b;
    if (a>b) {
        cout << a << " is the maximum number!" << endl;
    }
    else {
        cout << b << " is the maximum number!" << endl;
    }
}